class User
  def initialize(name,address,city,state,zip,country,email,userid,password)
    @name = name
    @address = address
    @city = city
    @state = state
    @zip = zip
    @country = country
    @email = email
    @userid = userid
    @password = password
  end

  def name
    @name
  end

  def address
    @address
  end

  def city
    @city
  end

  def state
    @state
  end

  def zip
    @zip
  end

  def country
    @country
  end

  def email
    @email
  end

  def userid
    @userid
  end

  def password
    @password
  end

end
